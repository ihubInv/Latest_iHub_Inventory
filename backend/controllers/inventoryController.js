// @desc    Bulk update inventory items
// @route   PUT /api/inventory/bulk-update
// @access  Private (Admin/Stock Manager)
const bulkUpdateInventoryItems = asyncHandler(async (req, res) => {
  const { items, updates } = req.body;

  if (!items || !Array.isArray(items) || items.length === 0) {
    return res.status(400).json({
      success: false,
      message: 'Items array is required'
    });
  }

  const results = [];
  const errors = [];

  for (const itemId of items) {
    try {
      const inventoryItem = await InventoryItem.findById(itemId);

      if (!inventoryItem) {
        errors.push({ itemId, error: 'Item not found' });
        continue;
      }

      // Update fields
      Object.keys(updates).forEach(key => {
        if (updates[key] !== undefined) {
          inventoryItem[key] = updates[key];
        }
      });

      inventoryItem.lastmodifiedby = req.user.name;
      inventoryItem.lastmodifieddate = new Date();

      await inventoryItem.save();
      results.push(inventoryItem);
    } catch (error) {
      errors.push({ itemId, error: error.message });
    }
  }

  res.json({
    success: true,
    message: `Updated ${results.length} items successfully`,
    data: {
      updated: results.length,
      errors: errors.length,
      results,
      errors
    }
  });
});

// @desc    Get next serial number preview (current sequence + 1, read-only)
// @route   GET /api/inventory/next-serial-preview
// @access  Private
const getNextSerialPreview = asyncHandler(async (req, res) => {
  try {
    // Always sync counter with actual inventory count to ensure accuracy
    const totalItems = await InventoryItem.countDocuments();
    
    // Get current counter value
    let currentSequence = await SerialNumberCounter.getCurrentSequence();
    
    // If counter is less than actual item count, sync it
    // This handles cases where items were created before the counter system
    if (currentSequence < totalItems) {
      await SerialNumberCounter.findByIdAndUpdate(
        'global',
        { sequenceValue: totalItems },
        { upsert: true, new: true }
      );
      currentSequence = totalItems;
    }
    
    const nextSerial = currentSequence + 1;
    const nextSerialFormatted = nextSerial.toString().padStart(3, '0');
    
    res.json({
      success: true,
      data: {
        currentSequence,
        nextSerial,
        nextSerialFormatted
      }
    });
  } catch (error) {
    console.error('Error getting serial preview:', error);
    // Fallback: use inventory count directly
    try {
      const totalItems = await InventoryItem.countDocuments();
      const nextSerial = totalItems + 1;
      res.json({
        success: true,
        data: {
          currentSequence: totalItems,
          nextSerial,
          nextSerialFormatted: nextSerial.toString().padStart(3, '0')
        }
      });
    } catch (countError) {
      // Final fallback
      res.json({
        success: true,
        data: {
          currentSequence: 0,
          nextSerial: 1,
          nextSerialFormatted: '001'
        }
      });
    }
  }
});

// @desc    Get available asset names for requests (excluding pending/approved requests)
// @route   GET /api/inventory/available-asset-names
// @access  Private
const getAvailableAssetNames = asyncHandler(async (req, res) => {
  const Request = require('../models/Request');

  // Get all pending and approved requests to exclude their item types
  const pendingAndApprovedRequests = await Request.find({
    status: { $in: ['pending', 'approved'] }
  }).select('itemtype');

  // Create a set of item types that are currently requested or approved
  const requestedItemTypes = new Set(
    pendingAndApprovedRequests.map(req => req.itemtype.trim().toLowerCase())
  );

  // Get all available inventory items
  const availableItems = await InventoryItem.find({
    status: 'available',
    balancequantityinstock: { $gt: 0 }
  }).select('assetname');

  // Extract unique asset names
  const allAssetNames = new Set();
  availableItems.forEach(item => {
    if (item.assetname && item.assetname.trim()) {
      allAssetNames.add(item.assetname.trim());
    }
  });

  // Filter out asset names that have pending or approved requests
  const availableAssetNames = Array.from(allAssetNames).filter(
    assetName => !requestedItemTypes.has(assetName.toLowerCase())
  ).sort();

  res.json({
    success: true,
    count: availableAssetNames.length,
    data: availableAssetNames
  });
});

function formatUserSnapshot(u) {
  if (!u) return null;
  if (typeof u === 'object') {
    return {
      name: u.name || '—',
      email: u.email || '—',
      role: u.role || null,
      department: u.department || null,
    };
  }
  return { name: String(u), email: null, role: null, department: null };
}

function humanTransactionSummary(t) {
  const type = t.transactiontype;
  const assignee = t.issuedto && typeof t.issuedto === 'object' ? t.issuedto.name : null;
  if (type === 'issue') return assignee ? `Issued to ${assignee}` : 'Issued to assignee';
  if (type === 'return') return assignee ? `Return processed (${assignee})` : 'Returned to inventory';
  if (type === 'purchase') return 'Initial receipt / added to inventory';
  if (type === 'adjustment') return 'Stock adjustment';
  if (type === 'disposal') return 'Disposal';
  if (type === 'maintenance') return 'Maintenance';
  return `Transaction: ${type}`;
}

function humanAuditSummary(a) {
  const map = {
    item_created: 'Asset created',
    item_updated: 'Asset details updated',
    item_deleted: 'Asset deleted from inventory',
  };
  return map[a.action] || a.action.replace(/_/g, ' ');
}

// @desc    Chronological audit history (transactions + structured audit log)
// @route   GET /api/inventory/:id/audit-history
// @access  Private (Admin, Stock Manager)
const getAssetAuditHistory = asyncHandler(async (req, res) => {
  let item = await InventoryItem.findById(req.params.id).select('uniqueid assetname');
  const rawUniqueId =
    (typeof req.query.uniqueId === 'string' && req.query.uniqueId.trim()) ||
    (typeof req.query.uniqueid === 'string' && req.query.uniqueid.trim()) ||
    '';
  const qUnique = String(rawUniqueId).trim();

  const escapeRegex = (value) => value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const uniqueIdPattern = qUnique
    ? new RegExp(`^${escapeRegex(qUnique).replace(/\s+/g, '\\s+')}$`, 'i')
    : null;

  if (!item && qUnique) {
    item = await InventoryItem.findOne({
      $or: [{ uniqueid: qUnique }, { uniqueid: uniqueIdPattern }],
    }).select('uniqueid assetname');
  }
  if (!item) {
    return res.status(404).json({
      success: false,
      message: 'Inventory item not found',
    });
  }

  const itemId = item._id;

  const [audits, txs] = await Promise.all([
    AssetAuditLog.find({ inventoryitemid: itemId }).sort({ occurredAt: 1 }).lean(),
    InventoryTransaction.find({ inventoryitemid: itemId })
      .sort({ transactiondate: 1, createdAt: 1 })
      .populate('issuedto', 'name email department role')
      .populate('issuedby', 'name email department role')
      .lean(),
  ]);

  const events = [];

  txs.forEach((t) => {
    events.push({
      source: 'transaction',
      id: String(t._id),
      at: t.transactiondate || t.createdAt,
      kind: t.transactiontype,
      summary: humanTransactionSummary(t),
      actor: formatUserSnapshot(t.issuedby),
      assignee: formatUserSnapshot(t.issuedto),
      quantity: t.quantity,
      previousQuantity: t.previousquantity,
      newQuantity: t.newquantity,
      purpose: t.purpose || null,
      remarks: t.notes || null,
      transactionStatus: t.status,
      condition: t.condition || null,
      expectedReturnDate: t.expectedreturndate || null,
      actualReturnDate: t.actualreturndate || null,
    });
  });

  audits.forEach((a) => {
    events.push({
      source: 'audit',
      id: String(a._id),
      at: a.occurredAt,
      kind: a.action,
      summary: humanAuditSummary(a),
      actor: a.actor,
      subject: a.subject || null,
      changes: a.changes || [],
      remarks: a.remarks || null,
      metadata: a.metadata || null,
    });
  });

  events.sort((x, y) => new Date(x.at) - new Date(y.at));

  res.json({
    success: true,
    data: {
      inventoryItemId: String(item._id),
      uniqueid: item.uniqueid,
      assetname: item.assetname,
      events,
    },
  });
});

