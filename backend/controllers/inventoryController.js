// Helper function to generate asset code from asset name
const generateAssetCode = (assetName) => {
  if (!assetName) return '---';
  // Take first 3 letters and convert to uppercase, remove non-alphabetic characters
  return assetName.replace(/[^a-zA-Z]/g, '').substring(0, 3).toUpperCase().padEnd(3, '-');
};

/**
 * Generate unique ID with automatically incremented global serial number
 * 
 * Serial numbers are ALWAYS auto-generated by the system based on total asset count.
 * - Each new asset receives the next sequential serial number (001, 002, 003, ...)
 * - Serial numbers increment automatically - no manual input required or allowed
 * - The serial number reflects the total number of assets in the system
 * - Uses atomic counter to ensure no duplicates even under concurrent requests
 * 
 * @param {string} financialyear - Financial year (e.g., "2025-26")
 * @param {string} assetname - Asset name (e.g., "Software")
 * @param {string} locationofitem - Location (e.g., "Storage Room A")
 * @returns {Promise<string>} Unique ID with auto-generated serial number
 */
const generateUniqueIdWithGlobalSerial = async (financialyear, assetname, locationofitem) => {
  // Get next global serial number atomically (automatically increments based on asset count)
  const serialNumber = await SerialNumberCounter.getNextSequence();
  const serialNumberStr = serialNumber.toString().padStart(3, '0');
  
  // Build unique ID: iHUB/{financialyear}/{assetcode}/{location}/{serial}
  let uniqueId = 'iHUB/';
  
  // Add financial year or placeholder
  if (financialyear) {
    uniqueId += financialyear;
  } else {
    uniqueId += '--';
  }
  uniqueId += '/';
  
  // Add asset code or placeholder
  if (assetname) {
    uniqueId += generateAssetCode(assetname);
  } else {
    uniqueId += '---';
  }
  uniqueId += '/';
  
  // Add location or placeholder
  if (locationofitem) {
    uniqueId += locationofitem.trim();
  } else {
    uniqueId += '--';
  }
  uniqueId += '/';
  
  // Add global serial number
  uniqueId += serialNumberStr;
  
  return uniqueId;
};

// @desc    Create inventory item
// @route   POST /api/inventory
// @access  Private (Admin/Stock Manager)
const createInventoryItem = asyncHandler(async (req, res) => {
  // Validate location if provided
  if (req.body.locationid) {
    const location = await Location.findById(req.body.locationid);
    if (!location) {
      return res.status(400).json({
        success: false,
        message: 'Invalid location ID'
      });
    }
    if (!location.isActive) {
      return res.status(400).json({
        success: false,
        message: 'Cannot assign item to inactive location'
      });
    }
  }

  // ALWAYS generate unique ID automatically with global serial number
  // Serial numbers are system-generated and cannot be manually set
  // This ensures sequential numbering based on asset count
  // Any manually provided uniqueid is ignored - system always generates it
  
  // Ensure counter is initialized/synced with actual inventory count
  const currentCounter = await SerialNumberCounter.getCurrentSequence();
  if (currentCounter === 0) {
    // Sync counter with actual inventory count if it's not initialized
    const totalItems = await InventoryItem.countDocuments();
    if (totalItems > 0) {
      await SerialNumberCounter.findByIdAndUpdate(
        'global',
        { sequenceValue: totalItems },
        { upsert: true, new: true }
      );
    }
  }
  
  // Always generate unique ID using global serial number (automatic, no manual input allowed)
  // The serial number is automatically incremented based on total asset count
  let uniqueId = await generateUniqueIdWithGlobalSerial(
    req.body.financialyear,
    req.body.assetname || req.body.assetnamefromcategory,
    req.body.locationofitem
  );
  
  // Verify the generated unique ID doesn't already exist (shouldn't happen, but safety check)
  const existingItem = await InventoryItem.findOne({ 
    uniqueid: uniqueId.toUpperCase().trim() 
  });
  
  if (existingItem) {
    // This is a rare race condition - regenerate with next number
    console.warn('Generated unique ID already exists, regenerating:', uniqueId);
    uniqueId = await generateUniqueIdWithGlobalSerial(
      req.body.financialyear,
      req.body.assetname || req.body.assetnamefromcategory,
      req.body.locationofitem
    );
  }

  // Convert empty productserialnumber to undefined to avoid validation issues
  const productserialnumber = req.body.productserialnumber?.trim();
  
  const inventoryData = {
    ...req.body,
    uniqueid: uniqueId,
    productserialnumber: productserialnumber && productserialnumber !== '' ? productserialnumber : undefined,
    createdby: req.user.id,
    lastmodifiedby: req.user.name
  };

  // Create inventory item
  // Note: productserialnumber is optional and can have duplicates
  let inventoryItem;
  try {
    inventoryItem = await InventoryItem.create(inventoryData);
  } catch (error) {
    // Handle MongoDB duplicate key error - only uniqueid should be unique
    if (error.code === 11000 && error.keyPattern && error.keyPattern.uniqueid) {
      return res.status(400).json({
        success: false,
        message: `Unique ID already exists: ${inventoryData.uniqueid}`
      });
    }
    // For all other errors (including any unexpected productserialnumber errors), re-throw
    // This will be handled by the asyncHandler error middleware
    throw error;
  }

  // Update location occupancy if location is specified
  if (inventoryItem.locationid) {
    try {
      const location = await Location.findById(inventoryItem.locationid);
      if (location) {
        await location.incrementOccupancy(inventoryItem.quantityperitem);
      }
    } catch (error) {
      console.error('Error updating location occupancy:', error);
    }
  }

  // Create initial transaction record
  await InventoryTransaction.create({
    inventoryitemid: inventoryItem._id,
    transactiontype: 'purchase',
    quantity: inventoryItem.quantityperitem,
    previousquantity: 0,
    newquantity: inventoryItem.balancequantityinstock,
    issuedby: req.user.id,
    purpose: 'Initial Purchase',
    notes: 'Item added to inventory',
    status: 'completed'
  });

  const populatedItem = await InventoryItem.findById(inventoryItem._id)
    .populate('assetcategoryid', 'name type')
    .populate('assetid', 'name description')
    .populate('locationid', 'name description address building floor')
    .populate('createdby', 'name email');

  await logAssetAudit({
    inventoryItemId: inventoryItem._id,
    assetUniqueId: inventoryItem.uniqueid,
    action: 'item_created',
    req,
    remarks: 'Asset registered in inventory',
    metadata: { assetname: inventoryItem.assetname },
  });

  res.status(201).json({
    success: true,
    message: 'Inventory item created successfully',
    data: populatedItem
  });
});

// @desc    Update inventory item
// @route   PUT /api/inventory/:id
// @access  Private (Admin/Stock Manager)
const updateInventoryItem = asyncHandler(async (req, res) => {
  const inventoryItem = await InventoryItem.findById(req.params.id);

  if (!inventoryItem) {
    return res.status(404).json({
      success: false,
      message: 'Inventory item not found'
    });
  }

  const bodyKeys = Object.keys(req.body).filter((k) => req.body[k] !== undefined);
  const beforeVals = {};
  bodyKeys.forEach((key) => {
    beforeVals[key] = inventoryItem[key];
  });

  // Update fields
  Object.keys(req.body).forEach(key => {
    if (req.body[key] !== undefined) {
      // Convert empty productserialnumber to undefined
      if (key === 'productserialnumber') {
        const value = req.body[key]?.trim();
        inventoryItem[key] = value && value !== '' ? value : undefined;
      } else {
        inventoryItem[key] = req.body[key];
      }
    }
  });

  if (req.body.financialyear !== undefined && inventoryItem.uniqueid) {
    const uniqueIdParts = inventoryItem.uniqueid.toUpperCase().split('/');
    if (uniqueIdParts.length === 5) {
      uniqueIdParts[1] = String(inventoryItem.financialyear).toUpperCase().trim();
      inventoryItem.uniqueid = uniqueIdParts.join('/');
    }
  }

  inventoryItem.lastmodifiedby = req.user.name;
  inventoryItem.lastmodifieddate = new Date();

  await inventoryItem.save();

  const serializeAuditValue = (val) => {
    if (val === undefined || val === null) return null;
    if (val instanceof Date) return val.toISOString();
    if (typeof val === 'object' && val !== null && val._id) return String(val._id);
    return val;
  };
  const auditValuesEqual = (a, b) =>
    JSON.stringify(serializeAuditValue(a)) === JSON.stringify(serializeAuditValue(b));

  const changes = [];
  bodyKeys.forEach((key) => {
    const from = beforeVals[key];
    const to = inventoryItem[key];
    if (!auditValuesEqual(from, to)) {
      changes.push({ field: key, from: serializeAuditValue(from), to: serializeAuditValue(to) });
    }
  });

  if (changes.length) {
    await logAssetAudit({
      inventoryItemId: inventoryItem._id,
      assetUniqueId: inventoryItem.uniqueid,
      action: 'item_updated',
      req,
      changes,
      remarks: '',
      metadata: { changeCount: changes.length },
    });
  }

  const populatedItem = await InventoryItem.findById(inventoryItem._id)
    .populate('assetcategoryid', 'name type')
    .populate('assetid', 'name description')
    .populate('locationid', 'name description address building floor')
    .populate('createdby', 'name email');

  res.json({
    success: true,
    message: 'Inventory item updated successfully',
    data: populatedItem
  });
});

// @desc    Delete inventory item
// @route   DELETE /api/inventory/:id?force=true
// @access  Private (Admin, Stock Manager)
const deleteInventoryItem = asyncHandler(async (req, res) => {
  const inventoryItem = await InventoryItem.findById(req.params.id);

  if (!inventoryItem) {
    return res.status(404).json({
      success: false,
      message: 'Inventory item not found'
    });
  }

  const forceDelete =
    req.query.force === 'true' ||
    req.query.force === '1' ||
    String(req.query.force || '').toLowerCase() === 'true';

  const isIssued = inventoryItem.status === 'issued';

  if (isIssued && !forceDelete) {
    return res.status(400).json({
      success: false,
      code: 'ITEM_IN_USE',
      message:
        'This asset is currently issued. To remove it from inventory you must confirm a full purge: all transactions, audit history, linked requests, and return requests for this asset will be permanently deleted.',
      details: {
        status: inventoryItem.status,
        issuedto: inventoryItem.issuedto || null,
        uniqueid: inventoryItem.uniqueid,
      },
    });
  }

  const itemId = inventoryItem._id;

  const session = await mongoose.startSession();
  try {
    await session.withTransaction(async () => {
      await AssetAuditLog.deleteMany({ inventoryitemid: itemId }, { session });
      await InventoryTransaction.deleteMany({ inventoryitemid: itemId }, { session });
      await Request.deleteMany({ inventoryitemid: itemId }, { session });
      await ReturnRequest.deleteMany({ inventoryitemid: itemId }, { session });
      await InventoryItem.findByIdAndDelete(itemId, { session });
    });

    res.json({
      success: true,
      message: 'Inventory item and all related records deleted successfully',
    });
  } catch (error) {
    console.error('Error during cascade delete:', error);
    res.status(500).json({
      success: false,
      message: 'Error deleting inventory item and related records',
    });
  } finally {
    await session.endSession();
  }
});

